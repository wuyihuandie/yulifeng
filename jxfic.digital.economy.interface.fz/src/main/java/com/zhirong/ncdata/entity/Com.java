package com.zhirong.ncdata.entity;

public class Com {
   private Long ID;

   private String ENTNAME;

   private String TYSHXDM;

   private String ADDRESS;

   private Double X;

   private Double Y;

    public Long getID() {
        return ID;
    }

    public void setID(Long ID) {
        this.ID = ID;
    }

    public String getENTNAME() {
        return ENTNAME;
    }

    public void setENTNAME(String ENTNAME) {
        this.ENTNAME = ENTNAME;
    }

    public String getTYSHXDM() {
        return TYSHXDM;
    }

    public void setTYSHXDM(String TYSHXDM) {
        this.TYSHXDM = TYSHXDM;
    }

    public String getADDRESS() {
        return ADDRESS;
    }

    public void setADDRESS(String ADDRESS) {
        this.ADDRESS = ADDRESS;
    }

    public Double getX() {
        return X;
    }

    public void setX(Double x) {
        X = x;
    }

    public Double getY() {
        return Y;
    }

    public void setY(Double y) {
        Y = y;
    }

    public Com() {
    }

    public Com(Long ID, String ENTNAME, String TYSHXDM, String ADDRESS) {
        this.ID = ID;
        this.ENTNAME = ENTNAME;
        this.TYSHXDM = TYSHXDM;
        this.ADDRESS = ADDRESS;
    }

    public Com(Long ID, String ENTNAME, String TYSHXDM, String ADDRESS, Double x, Double y) {
        this.ID = ID;
        this.ENTNAME = ENTNAME;
        this.TYSHXDM = TYSHXDM;
        this.ADDRESS = ADDRESS;
        X = x;
        Y = y;
    }
}