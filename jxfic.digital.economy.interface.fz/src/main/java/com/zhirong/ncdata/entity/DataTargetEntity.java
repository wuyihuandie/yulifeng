package com.zhirong.ncdata.entity;

import lombok.Data;

import java.io.Serializable;

@Data
public class DataTargetEntity implements Serializable {

    private String ID;          //指标id
    private String ACTNAME;     //指标分类名称
    private String ACTCODE;     //指标分类编码  CD01
    private String AREANAME;    //指标区域名称
    private String AREACODE;    //指标区域代码  CD02
    private String NAME;        //指标名称
    private String CODE;        //指标编码
    private String CYCLE;       //统计频率  天为00，周为01，月为02，季为03，半年为04，年为05
    private String UNIT;        //指标单位  数据单位,如万、户等
    private String DESCRIBE;    //指标描述
    private String CALCMODE;    //定量模式  绝对值为01,百分比为00
    private String STATE;       //指标状态  启动为01,停用为00
    private String DATATIME;    //指标创建时间
    private String SOURCE;      //数据来源，如统计局、市场监管局等
    private String TORDE;       //指标排序
}
