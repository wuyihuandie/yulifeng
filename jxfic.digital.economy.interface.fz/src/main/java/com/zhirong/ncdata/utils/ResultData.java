package com.zhirong.ncdata.utils;

import lombok.Data;

@Data
public class ResultData {
    private String message;
    private String status;
    private Object date;

    public ResultData() {
    }
    public ResultData(String message, String status, Object date) {
        this.message = message;
        this.status = status;
        this.date = date;
    }
}
